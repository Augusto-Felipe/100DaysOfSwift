import Foundation

// Arithmetic operators -> + - * /
let firstNumber = 10
let secondNumber = 9

// Soma
let total = firstNumber + secondNumber
print(total)

// Subtração
let difference = firstNumber - secondNumber
print(difference)

// Multiplicação
let product = firstNumber * secondNumber
print(product)

// Divisão
let divided = firstNumber / secondNumber
print(divided)

// Resto da divisão = modulo

let weeks = 465 / 7
let days = 465 % 7
print("There are \(weeks) weeks and \(days) days until the event.")

let rest = firstNumber % secondNumber
print(rest)

// INT VS DOUBLE
let myDouble: Double = 90000000000000001
let myInt: Int = 90000000000000001
print(myInt)
print(myDouble)




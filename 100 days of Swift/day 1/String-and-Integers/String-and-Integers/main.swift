import Foundation

// Swift é conhecido como uma linguagem de tipagem segura, o que significa que toda variável deve ser de um tipo específico.
// O Swift faz essa inferência de forma automática caso o desenvolvedor não especifique o seu tipo.

// Tipagem Implícita -> A linguagem swift vai entender que pelos dados que o usuário digitou como valor das variáveis abaixo, as mesma são do tipo INT.

var city = "Paris"
var game = "Street Fighter"
var job = "iOS Developer"
var country = "South Africa"
var age = 38
var population = 8_000_000
var numberOfDoors = 4
var numberOfWheels = 4
var speed = 88
var repeatCount = 82
var highScore = 328_556
var power = 9001
var rating = 5

// O Swift já deu um tipo para essas variáveis no momento que elas foram criadas, por isso não é possível mudar seu tipo.
// Ou seja, não posso pegar uma variável acima e colocar um tipo de valor diferente do seu tipo quando a mesma foi criada.

